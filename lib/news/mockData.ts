export function buildBreakingItem(index: number): NewsItem {
  const seed = breakingPool[index % breakingPool.length];
  return {
    ...seed,
    id: `tw-breaking-${Date.now()}`,
    timestamp: new Date().toISOString(),
    url: seed.url ?? mockSourceUrl(seed.source, seed.headline),
  };
}

